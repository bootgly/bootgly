namespace Bootgly\ACI\Tests\Benchmark;


use function array_key_first;
use function array_keys;
use function array_map;
use function array_slice;
use function bin2hex;
use function count;
use function date;
use function explode;
use function getmypid;
use function gmdate;
use function implode;
use function microtime;
use function number_format;
use function preg_replace;
use function random_bytes;
use function sprintf;
use function str_repeat;
use function trim;


/**
 * Markdown results report (`--results=report|charts`).
 *
 * Renders a collision-resistant `RESULTS-<load-set>-<run-token>.md` document — environment,
 * reproduction command, per-load comparison tables and peaks — and, when
 * charts are requested and the run swept an option, native SVG charts
 * (throughput / ratio / latency) via [[Chart]].
 *
 * Works on plain scalar arrays only (no Result objects) — the caller
 * extracts the run data before rendering.
 */
class Report
{
   // * Config
   /** Generate SVG charts alongside the Markdown document. */
   public protected(set) bool $charts;


   public function __construct (bool $charts = false)
   {
      // * Config
      $this->charts = $charts;
   }

   /**
    * Render the Markdown document.
    *
    * @param array{
    *    case: string,
    *    loadSet: string,
    *    metric: string,
    *    command: string,
    *    env: array<string,string>,
    *    config: array<string,scalar>,
    *    sweep: array<string,array<int,int>>,
    *    loads: array<int,string>,
    *    opponents: array<int,string>,
    *    data: array<string,array<string,array<int,null|float>>>,
    *    latencies: array<string,array<string,array<int,null|float>>>,
    *    marks: array<int,string>
    * } $run
    * @param array<int,string> $charts Chart file names to reference (same directory).
    */
   public function render (array $run, array $charts = []): string
   {
      $sweeping = $run['sweep'] !== [];
      $sweepKey = $sweeping ? array_key_first($run['sweep']) : null;
      $x = $sweeping ? $run['sweep'][$sweepKey] : [];
      $baseline = $run['opponents'][0] ?? '';

      $lines = [];

      // @ Title
      $title = count($run['opponents']) > 1
         ? "{$baseline} vs " . implode(' vs ', array_slice($run['opponents'], 1))
         : $baseline;
      $suffix = $sweeping ? " — sweep over `{$sweepKey}`" : '';
      $lines[] = "# {$run['case']} — {$run['loadSet']}: {$title}{$suffix}";
      $lines[] = '';
      $lines[] = "Generated by `--results` on " . date('Y-m-d H:i:s') . '.';
      $lines[] = '';

      // @ Environment
      $lines[] = '## Environment';
      $lines[] = '';
      foreach ($run['env'] as $label => $value) {
         $lines[] = "- **{$label}** — {$value}";
      }
      foreach ($run['config'] as $key => $value) {
         $lines[] = "- **{$key}** — `{$value}`";
      }
      $lines[] = '';

      // @ Reproduction command
      $lines[] = '## Command';
      $lines[] = '';
      $lines[] = '```bash';
      $lines[] = $run['command'];
      $lines[] = '```';
      $lines[] = '';

      // @ Charts
      foreach ($charts as $chart) {
         $lines[] = "![{$chart}]({$chart})";
         $lines[] = '';
      }

      // @@ Per-load tables
      $lines[] = '## Results';
      $lines[] = '';

      foreach ($run['loads'] as $load) {
         $lines[] = "### {$load}";
         $lines[] = '';

         // # Table header — one row per X point (sweep) or a single row
         $header = $sweeping ? "`{$sweepKey}`" : 'Run';
         $columns = ["| {$header} |"];
         foreach ($run['opponents'] as $opponent) {
            $columns[0] .= " {$opponent} |";
         }
         if (count($run['opponents']) > 1) {
            $columns[0] .= " Δ ({$baseline} vs " . ($run['opponents'][1] ?? '') . ') |';
         }
         $columns[] = '|' . str_repeat('---:|', count($run['opponents']) + ($sweeping ? 1 : 1) + (count($run['opponents']) > 1 ? 1 : 0));
         $lines = [...$lines, ...$columns];

         $points = $sweeping ? array_keys($x) : [0];
         foreach ($points as $i) {
            $label = $sweeping ? (string) $x[$i] : '—';
            $row = "| {$label} |";

            foreach ($run['opponents'] as $opponent) {
               $value = $run['data'][$load][$opponent][$i] ?? null;
               $row .= ' ' . ($value !== null ? number_format($value) : 'N/A') . ' |';
            }

            // # Δ vs the second opponent
            if (count($run['opponents']) > 1) {
               $a = $run['data'][$load][$baseline][$i] ?? null;
               $b = $run['data'][$load][$run['opponents'][1]][$i] ?? null;
               $row .= ' ' . ($a !== null && $b !== null && $b > 0.0
                  ? sprintf('%+.1f%%', (($a - $b) / $b) * 100)
                  : '—') . ' |';
            }

            $lines[] = $row;
         }

         $lines[] = '';
      }

      // @ Peaks
      $lines[] = '## Peaks';
      $lines[] = '';
      $lines[] = '| Load |' . implode('', array_map(
         static fn (string $opponent): string => " {$opponent} peak ({$run['metric']}" . ($sweeping ? " @ {$sweepKey}" : '') . ') |',
         $run['opponents']
      ));
      $lines[] = '|---|' . str_repeat('---|', count($run['opponents']));

      foreach ($run['loads'] as $load) {
         $row = "| {$load} |";

         foreach ($run['opponents'] as $opponent) {
            $peak = null;
            $at = null;
            foreach ($run['data'][$load][$opponent] ?? [] as $i => $value) {
               if ($value !== null && ($peak === null || $value > $peak)) {
                  $peak = $value;
                  $at = $sweeping ? $x[$i] : null;
               }
            }

            $cell = $peak !== null ? number_format($peak) : 'N/A';
            if ($peak !== null && $at !== null) {
               $cell .= " @ {$at}";
            }

            $row .= " {$cell} |";
         }

         $lines[] = $row;
      }
      $lines[] = '';

      // @ Marks
      $lines[] = '## Marks';
      $lines[] = '';
      foreach ($run['marks'] as $file) {
         $lines[] = "- `{$file}`";
      }
      $lines[] = '';

      // : Markdown document
      return implode("\n", $lines);
   }

   /**
    * Write the report (and charts) to a directory.
    *
    * @param string $dir Absolute output directory.
    * @param array{
    *    case: string,
    *    loadSet: string,
    *    metric: string,
    *    command: string,
    *    env: array<string,string>,
    *    config: array<string,scalar>,
    *    sweep: array<string,array<int,int>>,
    *    loads: array<int,string>,
    *    opponents: array<int,string>,
    *    data: array<string,array<string,array<int,null|float>>>,
    *    latencies: array<string,array<string,array<int,null|float>>>,
    *    marks: array<int,string>
    * } $run
    *
    * @return array<int,string> Written file names (report first, then charts).
    */
   public function save (string $dir, array $run): array
   {
      $loadSet = trim((string) preg_replace('/[^A-Za-z0-9._-]+/', '-', $run['loadSet']), '-.');
      $time = sprintf('%.6F', microtime(true));
      [$seconds, $fraction] = explode('.', $time, 2);
      $PID = getmypid();
      $token = gmdate('Y-m-d_His', (int) $seconds)
         . "-{$fraction}-p{$PID}-"
         . bin2hex(random_bytes(8));
      $base = "RESULTS-{$loadSet}-{$token}";
      $written = [];
      $chartFiles = [];

      // @ Charts — only meaningful with a swept X axis
      $sweeping = $run['sweep'] !== [];
      if ($this->charts && $sweeping) {
         $sweepKey = (string) array_key_first($run['sweep']);
         $x = $run['sweep'][$sweepKey];
         $baseline = $run['opponents'][0] ?? '';

         // # Throughput — one panel per load, one series per opponent
         $Throughput = new Chart(
            title: "{$run['case']} — {$run['loadSet']} throughput",
            xLabel: $sweepKey,
            yLabel: $run['metric'],
         );
         $chartFiles[] = "{$base}.chart.throughput.svg";
         Artifacts::commit("{$dir}/{$base}.chart.throughput.svg", $Throughput->render($x, $run['data']));

         // # Ratio — single panel, one series per load (baseline Δ% vs 2nd opponent)
         if (count($run['opponents']) > 1) {
            $second = $run['opponents'][1];
            $ratios = [];
            foreach ($run['loads'] as $load) {
               foreach ($x as $i => $_) {
                  $a = $run['data'][$load][$baseline][$i] ?? null;
                  $b = $run['data'][$load][$second][$i] ?? null;
                  $ratios[$load][$i] = $a !== null && $b !== null && $b > 0.0
                     ? (($a - $b) / $b) * 100
                     : null;
               }
            }

            $Ratio = new Chart(
               title: "{$baseline} vs {$second} — Δ% at each {$sweepKey}",
               xLabel: $sweepKey,
               yLabel: 'Δ%',
            );
            $chartFiles[] = "{$base}.chart.ratio.svg";
            Artifacts::commit("{$dir}/{$base}.chart.ratio.svg", $Ratio->render($x, ['Δ%' => $ratios]));
         }

         // # Latency — one panel per load, log scale
         $hasLatency = false;
         foreach ($run['latencies'] as $series) {
            foreach ($series as $values) {
               foreach ($values as $value) {
                  if ($value !== null) {
                     $hasLatency = true;
                     break 3;
                  }
               }
            }
         }
         if ($hasLatency) {
            $Latency = new Chart(
               title: "{$run['case']} — {$run['loadSet']} latency",
               xLabel: $sweepKey,
               yLabel: 'ms',
               yscale: 'log',
            );
            $chartFiles[] = "{$base}.chart.latency.svg";
            Artifacts::commit("{$dir}/{$base}.chart.latency.svg", $Latency->render($x, $run['latencies']));
         }
      }

      // @ Markdown document (references the chart files just written)
      $markdown = $this->render($run, $chartFiles);
      Artifacts::commit("{$dir}/{$base}.md", $markdown);

      // : Written file names — report first, then charts
      $written[] = "{$base}.md";
      foreach ($chartFiles as $file) {
         $written[] = $file;
      }

      return $written;
   }
}
